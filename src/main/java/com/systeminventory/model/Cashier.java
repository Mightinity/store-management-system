package com.systeminventory.model;

public class Cashier {

    private String cashierName;
    private String cashierNoPhone;
    private String cashierImageSource;
    private String cashierEmail;
    private String cashierPassword;
    private String cashierDateOfBirth;
    private String cashierAddress;

    public String getCashierName() {
        return cashierName;
    }

    public void setCashierName(String cashierName) {
        this.cashierName = cashierName;
    }

    public String getCashierNoPhone() {
        return cashierNoPhone;
    }

    public void setCashierNoPhone(String cashierNoPhone) {
        this.cashierNoPhone = cashierNoPhone;
    }

    public String getCashierImageSource() {
        return cashierImageSource;
    }

    public void setCashierImageSource(String cashierImageSource) {
        this.cashierImageSource = cashierImageSource;
    }

    public String getCashierEmail() {
        return cashierEmail;
    }

    public void setCashierEmail(String cashierEmail) {
        this.cashierEmail = cashierEmail;
    }

    public String getCashierPassword() {
        return cashierPassword;
    }

    public void setCashierPassword(String cashierPassword) {
        this.cashierPassword = cashierPassword;
    }

    public String getCashierDateOfBirth() {
        return cashierDateOfBirth;
    }

    public void setCashierDateOfBirth(String cashierDateOfBirth) {
        this.cashierDateOfBirth = cashierDateOfBirth;
    }

    public String getCashierAddress() {
        return cashierAddress;
    }

    public void setCashierAddress(String cashierAddress) {
        this.cashierAddress = cashierAddress;
    }
}
