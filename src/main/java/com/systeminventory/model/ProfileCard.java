package com.systeminventory.model;

public class ProfileCard {

    private String profileDetailsfullName;
    private String profileDetailsNoPhone;
    private String profileDetailsDateOfBirth;
    private String profileDetailsEmail;
    private String profileDetailsAddress;

    public String getProfileDetailsFullName() {
        return profileDetailsfullName;
    }

    public void setProfileDetailsfullName(String profileDetailsfullName) {
        this.profileDetailsfullName = profileDetailsfullName;
    }

    public String getProfileDetailsNoPhone() {
        return profileDetailsNoPhone;
    }

    public void setProfileDetailsNoPhone(String profileDetailsNoPhone) {
        this.profileDetailsNoPhone = profileDetailsNoPhone;
    }

    public String getProfileDetailsDateOfBirth() {
        return profileDetailsDateOfBirth;
    }

    public void setProfileDetailsDateOfBirth(String profileDetailsDateOfBirth) {
        this.profileDetailsDateOfBirth = profileDetailsDateOfBirth;
    }

    public String getProfileDetailsEmail() {
        return profileDetailsEmail;
    }

    public void setProfileDetailsEmail(String profileDetailsEmail) {
        this.profileDetailsEmail = profileDetailsEmail;
    }

    public String getProfileDetailsAddress() {
        return profileDetailsAddress;
    }

    public void setProfileDetailsAddress(String profileDetailsAddress) {
        this.profileDetailsAddress = profileDetailsAddress;
    }
}
